#!/usr/bin/env node

/**
 * Generate MCP Automations
 *
 * This script imports the compiled automation registry and generates
 * a static TypeScript file for the MCP server.
 *
 * Runs automatically after: cd functions && npm run build
 * Or manually: npm run generate:mcp-automations (from root)
 *
 * Prerequisites: Functions must be compiled (lib/ folder must exist)
 */

const fs = require("fs");
const path = require("path");

// Mock firebase-admin before importing
const mockDb = {
  collection: () => mockDb,
  doc: () => mockDb,
  get: () => Promise.resolve({ exists: false, data: () => null }),
  set: () => Promise.resolve(),
  update: () => Promise.resolve(),
  batch: () => ({
    set: () => {},
    update: () => {},
    commit: () => Promise.resolve(),
  }),
};

// Create mock modules
const mockFirebaseAdmin = {
  initializeApp: () => {},
  getFirestore: () => mockDb,
  FieldValue: {
    serverTimestamp: () => new Date(),
    arrayUnion: (...args) => args,
    arrayRemove: (...args) => args,
    increment: (n) => n,
  },
  Timestamp: {
    now: () => ({ toDate: () => new Date() }),
    fromDate: (d) => ({ toDate: () => d }),
  },
};

// Override require for firebase modules
const Module = require("module");
const originalRequire = Module.prototype.require;

Module.prototype.require = function (id) {
  if (id === "firebase-admin/firestore") {
    return mockFirebaseAdmin;
  }
  if (id === "firebase-admin/app") {
    return { initializeApp: () => {} };
  }
  if (id === "firebase-functions/v2/firestore") {
    return {
      onDocumentUpdated: () => () => {},
      onDocumentCreated: () => () => {},
      onDocumentDeleted: () => () => {},
    };
  }
  if (id === "firebase-functions/v2/https") {
    return {
      onCall: () => () => {},
      HttpsError: class extends Error {
        constructor(code, message) {
          super(message);
          this.code = code;
        }
      },
    };
  }
  if (id === "firebase-functions/v2/scheduler") {
    return {
      onSchedule: () => () => {},
    };
  }
  return originalRequire.apply(this, arguments);
};

// Now import the compiled registry
const registryPath = path.join(__dirname, "..", "functions", "lib", "automation", "automation-registry.js");

if (!fs.existsSync(registryPath)) {
  console.error("Error: Functions not built. Run 'npm run build' in functions/ first.");
  console.error(`Expected: ${registryPath}`);
  process.exit(1);
}

const { AUTOMATION_REGISTRY } = require(registryPath);

const OUTPUT_PATH = path.join(__dirname, "..", "mcp-server", "src", "generated", "automations.ts");

function generateFile() {
  const automations = Object.values(AUTOMATION_REGISTRY);

  const lines = [
    "// AUTO-GENERATED FILE - DO NOT EDIT",
    "// Generated by: scripts/generate-mcp-automations.js",
    `// Generated at: ${new Date().toISOString()}`,
    "//",
    "// To regenerate: npm run generate:mcp-automations",
    "",
    "// =============================================================================",
    "// TYPES",
    "// =============================================================================",
    "",
    'export type AutomationCategory = "matching" | "learning" | "sync" | "search" | "cleanup";',
    "",
    "export interface TriggerCondition {",
    "  field: string;",
    "  from?: unknown;",
    "  to: unknown;",
    "}",
    "",
    "export interface AutomationEffect {",
    "  entity: string;",
    "  fields: string[];",
    '  action: "create" | "update" | "delete";',
    "}",
    "",
    "export interface AutomationLearning {",
    "  entity: string;",
    "  fields: string[];",
    "  description: string;",
    "}",
    "",
    "export interface FirestoreTrigger {",
    '  type: "document_create" | "document_update" | "document_delete";',
    "  collection: string;",
    "  conditions?: TriggerCondition[];",
    "}",
    "",
    "export interface CallableTrigger {",
    '  type: "callable";',
    "  regions?: string[];",
    "}",
    "",
    "export interface ScheduledTrigger {",
    '  type: "scheduled";',
    "  schedule: string;",
    "}",
    "",
    "export type AutomationTrigger = FirestoreTrigger | CallableTrigger | ScheduledTrigger;",
    "",
    "export interface AutomationMeta {",
    "  id: string;",
    "  name: string;",
    "  description: string;",
    "  trigger: AutomationTrigger;",
    "  effects: AutomationEffect[];",
    "  learns?: AutomationLearning[];",
    "  config?: Record<string, number | string | boolean>;",
    "  chains?: string[];",
    "  icon?: string;",
    "  category: AutomationCategory;",
    "  aiPowered?: boolean;",
    "}",
    "",
    "// =============================================================================",
    "// AUTOMATIONS",
    "// =============================================================================",
    "",
    `export const AUTOMATIONS: AutomationMeta[] = ${JSON.stringify(automations, null, 2)};`,
    "",
    "// =============================================================================",
    "// HELPERS",
    "// =============================================================================",
    "",
    "export function getAutomation(id: string): AutomationMeta | undefined {",
    "  return AUTOMATIONS.find((a) => a.id === id);",
    "}",
    "",
    "export function getAutomationsByCollection(collection: string): AutomationMeta[] {",
    "  return AUTOMATIONS.filter((a) => {",
    '    if (a.trigger.type === "document_create" || a.trigger.type === "document_update" || a.trigger.type === "document_delete") {',
    "      return (a.trigger as FirestoreTrigger).collection === collection;",
    "    }",
    "    return false;",
    "  });",
    "}",
    "",
    "export function getAutomationsByCategory(category: AutomationCategory): AutomationMeta[] {",
    "  return AUTOMATIONS.filter((a) => a.category === category);",
    "}",
    "",
  ];

  return lines.join("\n");
}

function main() {
  console.log("Generating MCP automations...");
  console.log(`Registry has ${Object.keys(AUTOMATION_REGISTRY).length} automations`);

  const content = generateFile();

  // Ensure output directory exists
  const outputDir = path.dirname(OUTPUT_PATH);
  if (!fs.existsSync(outputDir)) {
    fs.mkdirSync(outputDir, { recursive: true });
  }

  fs.writeFileSync(OUTPUT_PATH, content, "utf-8");
  console.log(`Generated: ${OUTPUT_PATH}`);
}

main();
